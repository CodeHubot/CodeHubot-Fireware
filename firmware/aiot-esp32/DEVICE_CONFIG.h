/**
 * 产品标识符（Product ID）
 * 
 * ⚠️ 重要：此值必须与管理后台的产品标识一致！
 * 
 * 产品标识格式：
 * - 通常使用：硬件型号-功能-版本
 * - 示例：ESP32-S3-TempHumidity-01
 * 
 * 如何获取产品标识：
 * 1. 登录管理后台
 * 2. 进入"产品管理"页面
 * 3. 创建产品时设置的"产品标识"或"产品代码"
 * 
 * 常见产品标识示例：
 * - "ESP32-S3-Dev-01"           → ESP32-S3 开发板 v1
 * - "ESP32-S3-TempHumidity-01"  → 温湿度监测器 v1
 * - "ESP32-S3-SmartLight-02"    → 智能灯光控制器 v2
 * - "ESP32-C3-EnvMonitor-01"    → 环境监测站 v1
 * 
 * 💡 提示：产品标识用于：
 * - 在服务器端查找产品配置（传感器、控制端口等）
 * - 关联固件OTA更新
 * - 区分不同类型的设备
 */
#define DEVICE_PRODUCT_ID           "ESP32-S3-Lite-01"


/**
 * 固件版本号
 * 
 * 版本号格式：主版本.次版本.修订版本
 * 
 * 示例：
 * - "1.0.0"  → 第一个正式版本
 * - "1.0.1"  → 修复bug
 * - "1.1.0"  → 新增功能
 * - "2.0.0"  → 重大更新
 * 
 * ⚠️ 重要：
 * - 每次发布新版本时，必须修改此值
 * - 服务器会根据此值判断是否需要OTA更新
 * - 版本号必须递增（1.0.0 → 1.0.1 → 1.1.0 → 2.0.0）
 */
#define DEVICE_FIRMWARE_VERSION     "1.7"


// ============================================================================
// 💡 MAC地址说明
// ============================================================================
/*
 * ⚠️ MAC地址无需在此配置！
 * 
 * ESP32的MAC地址是硬件自带的，烧录在eFuse中，无法修改。
 * 
 * 获取MAC地址的方法：
 * 1. 查看LCD屏幕显示（启动时会显示）
 * 2. 查看配网页面（连接WiFi热点后访问 192.168.4.1）
 * 3. 查看串口日志（使用 idf.py monitor）
 * 
 * MAC地址格式示例：80:B5:4E:D6:F8:60
 */


// ============================================================================
// 🎯 快速配置向导
// ============================================================================
/*
 * 烧录新设备的步骤：
 * 
 * 1. 修改本文件：
 *    - DEVICE_PRODUCT_ID = 你的产品ID
 *    - DEVICE_FIRMWARE_VERSION = 当前固件版本
 * 
 * 2. 编译固件：
 *    cd firmware/aiot-esp32
 *    idf.py build
 * 
 * 3. 烧录固件：
 *    idf.py flash
 * 
 * 4. 查看日志（获取MAC地址）：
 *    idf.py monitor
 *    
 *    或使用一次性命令：
 *    idf.py flash monitor
 * 
 * 5. 在日志中找到MAC地址，复制并打印贴在设备上
 * 
 * 6. 在管理后台注册设备：
 *    - 设备注册页面
 *    - 输入MAC地址
 *    - 选择对应的产品ID
 *    - 提交注册
 */


